# Addition
def add_numbers(a, b):
    
        sum = a + b
        return sum

# Subtraction
def sub_numbers(a, b):
    
        sum = a - b
        return sum

# Division
def div_numbers(a, b):
    
        sum = a / b
        return sum

# Multiplication
def mul_numbers(a, b):
   
        sum = a * b
        return sum